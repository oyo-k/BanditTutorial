using Random

# ============================================================
# バンディット問題とは？
# ============================================================
# カジノのスロットマシン（バンディット）が複数台あるとき、
# どの台を引けば報酬をもらいやすいかを、実際に試しながら
# 学んでいく問題を「多腕バンディット問題」という。
#
# このファイルでは
#   1. softmax_choice  : 学習した価値をもとに腕を選ぶ関数
#   2. run_bandit      : Q 学習で腕を学習するシミュレーション本体
# の 2 つを定義する。
# ============================================================


# ============================================================
# Q 値（行動価値）とは？
# ============================================================
# 各腕に対して「この腕はどれくらい良さそうか」という期待値を
# 数値で持ったものを Q 値（Q-value）と呼ぶ。
# 最初はすべて 0.0 からスタートし、試行を重ねるごとに更新される。
# ============================================================


"""
    softmax_choice(q, beta) -> Int

Q 値ベクトル `q` から softmax（逆温度 `beta`）で行動を 1 つ選択し、
選ばれた腕のインデックスを返す。
"""
function softmax_choice(q, beta)
    # --- softmax 変換 ---
    # Q 値が高い腕ほど選ばれやすくなるよう、確率に変換する。
    #
    # 手順：
    #   1. 各 Q 値を beta 倍して指数関数に通す（大きな差を増幅）
    #   2. 全腕の合計で割って、合計が 1.0 になる確率分布にする
    #
    # beta（逆温度）の意味：
    #   小さい → 腕の差を無視してランダムに選ぶ（探索的）
    #   大きい → Q 値が最大の腕をほぼ確実に選ぶ（活用的）
    w = exp.(beta .* q)       # 各腕の重み（スカラー倍 + 指数変換）
    p = w ./ sum(w)           # 合計が 1 になるよう正規化 → 選択確率

    # --- ルーレット選択 ---
    # rand() で 0〜1 の乱数を生成し、
    # 確率の累積和（cumsum）が乱数を初めて超えた腕を選ぶ。
    # 例: p = [0.3, 0.5, 0.2] なら cumsum = [0.3, 0.8, 1.0]
    #   rand() = 0.6 のとき → 最初に 0.8 >= 0.6 となる 2 番目の腕を選択
    return findfirst(cumsum(p) .>= rand())
end


"""
    run_bandit(n_trials, reward_probs, alpha, beta) -> NamedTuple

多腕バンディット上で Q 学習を `n_trials` 試行ぶん実行する。
`(accuracy=..., regret=...)` の NamedTuple を返す。

- `reward_probs` : 各腕の報酬確率
- `alpha`        : 学習率（0〜1。大きいほど直近の結果を重視）
- `beta`         : softmax の逆温度（大きいほど貪欲に最良腕を選ぶ）
"""
function run_bandit(n_trials, reward_probs, alpha, beta)
    n_arms   = length(reward_probs)  # 腕の本数（例：2）
    q        = fill(0.0, n_arms)     # Q 値の初期化（全腕を 0.0 に設定）
    accuracy = zeros(n_trials)       # 各試行で最適腕を選んだか（1/0）を記録
    regret   = zeros(n_trials)       # 各試行の後悔（regret）を記録
    best_arm = argmax(reward_probs)  # 正解の腕（報酬確率が最大の腕）
    p_best   = reward_probs[best_arm]  # 最適腕の報酬確率

    # ============================================================
    # regret（後悔）とは？
    # ============================================================
    # 毎試行、「もし最初から最良腕を引き続けていたら」との差を表す。
    #
    #   regret_t = p_best - p_choice
    #
    # 例: p_best=0.8, p_choice=0.2 → regret = 0.6
    #     最良腕を選んだとき              → regret = 0.0
    #
    # 累積 regret = Σ regret_t（右上がりのカーブで増えていく）
    # 学習が早いほどカーブが緩やかになり、「少ない損失で済んだ」ことを示す。
    # ============================================================

    for t in 1:n_trials
        # --- 行動選択 ---
        # 現在の Q 値をもとに softmax で腕を 1 本選ぶ
        choice = softmax_choice(q, beta)

        # --- 報酬の受け取り ---
        # 選んだ腕の報酬確率に従って、報酬 1 または 0 を得る
        # （例: 確率 0.8 の腕なら 80% の確率で reward = 1）
        reward = rand() < reward_probs[choice] ? 1 : 0

        # --- Q 値の更新（Q 学習の中心部分）---
        # 「予測誤差 = 実際の報酬 - 現在の Q 値」を計算し、
        # その誤差の alpha 割だけ Q 値を修正する。
        #
        #   新しい Q = 古い Q + alpha × (報酬 - 古い Q)
        #
        # 例: Q = 0.5, reward = 1, alpha = 0.3 なら
        #       0.5 + 0.3 × (1 - 0.5) = 0.65 に更新
        # 良い結果が出た腕の Q 値は上がり、次に選ばれやすくなる。
        q[choice] += alpha * (reward - q[choice])

        # --- 正解判定と後悔の記録 ---
        accuracy[t] = choice == best_arm ? 1.0 : 0.0
        regret[t]   = p_best - reward_probs[choice]
    end

    return (accuracy=accuracy, regret=regret)
end
